package edu.hebeu.po;

public class StuAndCou {
	
	private String account;
	private String cou1;
	private String cou2;
	private String cou3;
	private String cou4;
	private String cou5;
	private String cou6;

	public StuAndCou(String account, String cou1, String cou2, String cou3, String cou4, String cou5, String cou6) {
		super();
		this.account = account;
		this.cou1 = cou1;
		this.cou2 = cou2;
		this.cou3 = cou3;
		this.cou4 = cou4;
		this.cou5 = cou5;
		this.cou6 = cou6;
	}

	public StuAndCou() {
		super();
	}
	
	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}
	
	public String getCou1() {
		return cou1;
	}

	public void setCou1(String cou1) {
		this.cou1 = cou1;
	}

	public String getCou2() {
		return cou2;
	}

	public void setCou2(String cou2) {
		this.cou2 = cou2;
	}

	public String getCou3() {
		return cou3;
	}

	public void setCou3(String cou3) {
		this.cou3 = cou3;
	}

	public String getCou4() {
		return cou4;
	}

	public void setCou4(String cou4) {
		this.cou4 = cou4;
	}

	public String getCou5() {
		return cou5;
	}

	public void setCou5(String cou5) {
		this.cou5 = cou5;
	}

	public String getCou6() {
		return cou6;
	}

	public void setCou6(String cou6) {
		this.cou6 = cou6;
	}

}
